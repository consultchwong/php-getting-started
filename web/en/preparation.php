<h2>Introduction</h2>

<div align="justify">This information is intended as a general guide to the preparations you should make for your elective adult circumcision and the after-care of your newly circumcised penis. The guidance also applies to the circumcision of pubescent and post-pubescent teenagers but not to infant or childhood circumcisions. 
  <br>
  <br>
  There are no hard and fast rules regarding how to prepare for a circumcision, nor what to do and expect after it so the following information is only a guide. This information has been gathered from a variety of sources including hints given on the Internet by newly circumcised men recounting what worked well for them. 
