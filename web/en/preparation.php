<h2 align="justify"><a name="after" />After-care</h2>

<div align="justify">After the operation your whole penis will be swollen and look very bruised. This is a normal effect of both the injected anaesthetic and the handling it necessarily receives during the operation. These will gradually reduce over the next week or two. 
  <br>
  <br>
  
