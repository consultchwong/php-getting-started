    <td width="61%" valign="top" id="#5"><p><strong class="t">Preauricular sinus (sinus)</strong><br />
<p class="style1">Preauricular sinus are common congenital malformations first described by Heusinger in 1864. Preauricular sinus are frequently noted on routine physical examination as small dells adjacent to the external ear, usually at the anterior margin of the ascending limb of the helix. However, preauricular sinus have been reported to occur along the lateral surface of the helicine crus and the superior posterior margin of the helix, the tragus, or the lobule. Anatomically, preauricular sinus are lateral and superior to the facial nerve and the parotid gland. </p>

<p class="t">Surgical Care<br /></p>

<p class="style1">Once infection occurs, the likelihood of recurrent acute exacerbations is high, and the sinus tract should be surgically removed.Surgery should take place once the infection has been treated with antibiotics and the inflammation has had time to subside. Controversy regarding indications for surgery exists. Some believe that the sinus tract should be surgically extirpated in patients who are asymptomatic because the onset of symptoms and subsequent infection cause scarring, which may lead to incomplete removal of the sinus tract and postoperative recurrences. The recurrence rate after surgery is 13-42% in smaller studies and 21% in one large study.</p>

<p class="style1">Most postoperative recurrences occur because of incomplete removal of the sinus tract. One way to prevent incomplete removal is to properly delineate the tract during surgery. Some surgeons cannulate the orifice and inject methylene blue dye into the tract 3 days prior to surgery under sterile conditions. The opening is then closed with a purse-string suture. This technique distends the tract and its extensions by its own secretion stained with methylene blue.</p>

<p class="style1">During surgery, some surgeons use either a probe or an injection of methylene blue dye for cannulation of the orifice. The most successful method is to use both modalities to delineate the entire tract. </p>


