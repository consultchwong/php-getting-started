                    <td valign="top" id="#3"><strong class="t">治療方法</strong><br />
                      <br />
                      <p><span class="btype">治療疝氣</span>, 吃藥、物理治療、疝氣帶都無濟於事。且會延遲治療時機, 可以令腸子或卵巢掉入腹股溝而回不來，令問題更嚴重。 治療唯一的方法就是手術。但是, 疝氣發生在兩歲以下的幼兒, 而且症狀輕微, 專科醫生會建議 "先觀察不手術", 其他原因均應該手術治療。</p>
					  
					  <p><span class="btype">小腸氣</span> (疝氣)與陰囊水腫手術方式相同，都在腹股溝位置做一個約1.5厘米橫切口，將疝氣囊與周邊組織分離結紮，除極少數例外，一般兒童疝氣不必做腹股溝後壁筋膜修補。</p>

					  <p><span class="btype">睪丸固定手術</span> 與疝氣手術方式類似，但必須在陰囊部位另外做一切口，將延長之睪丸與精索埋入。但是大部分睪丸固定位置，與正常人相同。風險相對低，手術輸血之可能性極低。除極少數例外，一般採用全身麻醉。</p>

