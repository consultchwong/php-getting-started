                      <br />
                      <p><span class="btype">手術</span> 多多少少會經歷一些不適，而外科手術難免包括一些風險。因此手術前要了解小孩子手術可能的不適或是併發症。在簽下手術同意書前，盡量發問問題。</p>
					  
					  <p><span class="btype">常見的術後不適：</span><br />
					  傷口會疼痛、酸麻、腫脹<br />
					  噁心、嘔吐<br />
					  焦躁、失眠<br />
					  口渴<br />
					  便秘或一直放屁<br />
					  </p>
					  
					  <p><span class="btype">手術後</span> ，醫生將會決定孩子何時可以進食。進食最好慢慢開始，從液態食物，如水、果汁等等。如果沒有嘔吐，接著可以開始吃較容易消化的食物，像是湯、稀飯、蛋。不要太強迫孩子吃多，但是要鼓勵喝水。</p>				    </td>
