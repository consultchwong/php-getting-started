    <td width="61%" valign="top" id="#1"><p><strong class="t">幼兒(BB)小腸氣治療策略</strong><br />
        <br />
        <span class="btype">現時</span>, 因小腸氣(疝氣) 前來診症的幼兒, 最終確診為以下三種常見的幼兒病患, 因為徵狀相似, 常被一般人誤解而延誤診症以致病情惡化, 嚴重者可引致腸壞死及不育。
		<p><span class="btype">小腸氣(疝氣)</span> 依不同年齡層，發生率由5至28%不等。但是經適當的麻醉及手術修補，擁有極低的危險及復發率，因此疝氣一經診斷，若病人生理狀態穩定，應考慮手術修補。</p>
		
		<p><span class="btype">陰囊水腫</span> 形成原因與疝氣類似，但無腸壞死之顧慮。若觀察至1~2歲仍未自行吸收，仍應考慮手術修補。</p>
		
		<p><span class="btype">隱睪症</span> 則是因為睪丸未下降至陰囊，可能導致睪丸造精細胞受損，從而不孕。觀察至6個月至1歲睪丸仍未自動下降者，應考慮睪丸固定手術。</p>
		
