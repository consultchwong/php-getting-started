                    <td valign="top" id="#4"><strong class="t">兒童手術須知</strong><br />
                      <br />
                      <p><span class="btype">誠實面對</span> - 可能您想要騙一騙他（她），但是對他（她）誠實些可能會更好。告訴他（她）爲什麼他（她）得要上醫院。向他（她）解釋即使有些不舒服，醫生護士會盡力幫他（她）。</p>
                      <p><span class="btype">小心使用字眼</span> - 跟孩子解釋要用簡單的話。譬如：醫生要幫你「令肚子受到保護」，而不是醫生要幫你把肚子的東西「割掉」/  「在肚子縫針/修補」。對外科而言，「保護」要比「縫針/修補」要好。解釋麻醉不要用「醫生要讓你睡覺」，有時孩子會由此聯想到死亡。可以說「醫生要幫你睡個午覺，一下下就會醒來」</p>
					  <p><span class="btype">鼓勵孩子發問</span> - 跟他（她）討論手術後可能會發生的狀況，問問他（她）有沒有任何想問的問題。你也可以自己問他（她）一些問題，像是：
					  <p>1. 對於接受手術或是要住院，哪件事最困難、最不舒服？</p>
					  <p>2. 哪件事對你最簡單、最容易接受？</p>
					  <p>3.對於接受手術、或是要住院，哪件事你覺得最好？</p>
					  <p>4.需要住院？或只是門診手術不住院？</p>
					  <p>5. 還有其他問題嗎？</p> <br />
					  </p>
					  					  
					  <p><span class="btype">向她（他）保證會沒事</span> - 要強調待在醫院只是暫時的；只要醫生認為病情穩定，就可以回家。也要讓她（他）知道「醫院」和「家」的相似之處：三餐照常、有遊戲空間、還有自己一張床。有些小孩會認為是自己「不乖」，所以才來醫院受「懲罰」。您必須要跟她（他）保證是無關的。</p>
					  
					  <p><span class="btype">讓孩子的感情能宣洩</span> - 孩子也許會覺得生氣、悲傷、害怕、或是有混雜這些感覺的情緒。您不要認為這些情緒是錯誤的而感到生氣，您只要確定這只是一個情緒發洩的管道就好了。告訴他（她）害怕是很正常的，哭哭也沒有關係。</p>

					  <p><span class="btype">容許孩子的意見</span> - 當準備住院的行李時，可以讓孩子決定帶幾件他（她）的「個人物品」，如玩具、書、小棉被、音樂帶、CD、遊戲機等等。</p>
					  
					  
					  <p><span class="btype">有耐心</span> - 每個人當面臨未知的恐懼時，表現就會不如平時。有的人會哭鬧不休、焦躁、行為退化（尿床、吸手指）、甚至平常睡覺不怕黑的孩子也怕黑了。記住，當這些焦慮消失後，一切會恢復正常。</p>
					  
					  
					  <p><span class="btype">保重自己</span> - 對自己也要好一些。將自己的生活簡化，日常生活瑣事找人分擔。多休息，充分飲食。尋求家人、朋友的幫忙。</p>
					  
					  
