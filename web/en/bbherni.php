					<p><span class="btype">2 - </span> 吃些易消化和含纖維素多的食品，以保持大便通暢。孩子大便乾燥時，應採取通便措施，不要讓孩子用力解大便。</p>
					
					<p><span class="btype">3 - </span> 不要讓孩子大聲咳嗽，患咳嗽的小兒要在醫生指導下適當吃些止咳藥。避免孩子大聲啼哭，防止腹壓升高。</p>

