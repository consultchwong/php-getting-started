<p style="letter-spacing:2px">（法新社墨西哥市八日電） 根據兩項昨天在國際愛滋病大會發表的研究，割包皮對於使男性免於感染愛滋病毒的功效，似乎超過原本預期，同時對一種常見的性病還有部份預防的效果。芝加哥伊利諾大學的美國 研究員貝里提出來自肯亞基蘇姆的長期實驗數據。這項實驗在初期，以兩千七百八十四名未割包皮也未感染愛滋病毒的男子為對象。這些人有半數接受割禮，其餘則延後接受割禮，並接受愛滋病毒檢驗。之前公布的研究指出，實驗兩年時，割包皮的人感染人類免疫缺乏病毒（愛滋病毒）的風險，比未割包皮的人少了百分之五十九。 兩年的數據顯示割包皮的效果令人震驚，而研究人員提議為先前未接受手術的研究對象進行割禮，因為如果不那麼做，於道德有所虧欠。 貝里在第十七屆國際愛滋病大會報告說，經過更詳細檢測血液樣本，在實驗開始兩年後，割包皮手術對預防愛滋病的效果評估已修正為百分之六十。
