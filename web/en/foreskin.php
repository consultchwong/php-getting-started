					  <span class="btype">行路唔似機械人</span> - 割包皮是將多出的包皮拿走。 而傳統的手術護理， 會用半個紙杯罩着傷口，以免組織黏連，但由於紙杯體積大，即使穿着鬆身衣服，病人下體仍會凸起，加上走動時若碰到紙杯及傷口，也會引致痛楚, 在未完全康復期間, 常見病人"行路似機械人" 般。 <br /><br />
約於02年, 一款人造皮膠布面世， 可取代紙杯作為割包皮的術後敷料，方便病人隨時接受手術，人造皮膠布由一種特別的物質製造，可緊貼在傷口或灼傷的地方，膠布黏貼力強，不易鬆脫，表面的一層網可避免黏着傷口，可減低除去膠布時的痛楚，更不會影響外觀 。
人造皮膠布具透氣及防水特性，故病人在接受割包皮手術後仍可洗澡，但只可以淋浴，不可浸浴。 不少病人周五接受手術，下周一已可上學/上班 ，但使用人造皮膠布不會縮短傷口的癒合時間，每五至七日需要更換膠布一次，同時也要避免劇烈運動 。					  
					  
