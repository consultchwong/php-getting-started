    <td width="61%" valign="top" id="#3"><p><strong class="t">瘜肉( 皮膚瘜肉)</strong><br />
<p class="style1">皮膚瘜肉，這個相當常見的皮膚問題，長久以來就是許多女性心中的痛。因為往往在卅歲前後就開始陸陸續續地出現在脖子，讓許多愛美的女性到了夏天寧願熱得汗流浹背，說什麼也不肯穿上低領的衣服。</p>

<p class="style1">典型皮膚瘜肉的表現，會在脖子出現一個個膚色小腫瘤。一開始它可能只是直徑約一至二厘米的小突起，但是漸漸地它會稍稍長大，形成有莖性懸垂的腫瘤。</p>

<p class="style1">初得皮膚瘜肉的人，通常都會無法接受會繼續慢慢長出皮膚瘜肉的現實，而在門診中質問這個疾病的病因，希望可以藉此知道預防的方法！</p>

<p class="style1">但是很可惜的，目前皮膚瘜肉的確切成因，仍然是個謎。儘管皮膚科醫師提出很多的理論，還是無法了解為何會莫名地產生纖維組織的增生，造成肉眼看到的一顆顆膚色小腫瘤。但是可以確定的，它絕對和任何的細菌病毒無關，所以當然不會傳染，更對健康無害。</p>

<p class="style1">在過去雷射還不是很發達的時代，皮膚科醫師多半都會勸病人不要管它，等到真的很大時才幫病人剪掉，或者是在局部麻醉下用電燒燒掉。</p>

<p class="style1">所幸這樣的情況在一些像是鉺雅各雷射和二氧化碳雷射的發明之後已經大為改觀。這些雷射可以在能量控制得宜下迅速地將小型的皮膚瘜肉汽化掉，只有些微的疼痛感。所以皮膚贅瘤不用再苦等到有根莖才處理，可以每隔一兩年就將小小的新生贅瘤清理乾淨，用光滑的脖子見人！</p>
