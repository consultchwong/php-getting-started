    <td width="61%" valign="top" id="#1"><p><strong class="t">粉瘤</strong><br />
<p class="style1">粉瘤醫學名稱是 表皮囊腫, 指的是一種皮膚深部囊狀的良性腫瘤。因為腫瘤的囊中通常是包裹著許多乳白顏色的粉狀物質，故以粉瘤稱之。好發於三、四十歲的男性，男性與女性發生比率約為2:1，常發生於皮脂腺豐富的部位，如臉、頸部、背部、陰部及曾經有較嚴重青春痘或皮膚受傷過的部位等。</p>
<p class="style1">如果粉瘤在短時間內快速長大、長時間摩擦壓迫，或者本身抵抗力較弱、身體疲累時，往往較容易造成疼痛、發炎感染、潰瘍、破裂流膿等症狀。在老年人、糖尿病患者、免疫機能障礙患者，甚至會引發筋膜炎、敗血症，危及生命。</p>
<p class="style1">治療時需視粉瘤的大小及部位而採取不同的方式，有局部藥物注射、切開引流、手術摘除、雷射治療等。要強調的是, 手術要求粉瘤囊壁一定要完整切除，否則很易復發。 平時要避免過度日曬，使用清爽不油膩的皮膚保養品；當發現有腫塊時，不要去刺激或擠壓，以免發炎感染。</p>
<p class="style1">最後，如果粉瘤生長快速、破裂疼痛、影響外觀，或是生長在容易摩擦部位者，或本身抵抗力較弱的患者，應該積極處理，儘早切除化驗，以免事態擴大。</p>

