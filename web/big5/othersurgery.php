    <td width="61%" valign="top" id="#4"><p><strong class="t">雞眼</strong><br /><p class="style1">雞眼是由於反覆的磨擦和壓迫所造成的，形成境界鮮明、堅硬、黃色的增生角質丘疹或結節，大小通常為數毫米，好發於關節突出處，例如腳底足骨末端，以及小腳趾的外側、腳趾縫。雞眼就像是腳底黏著一個小石頭，只要壓迫到就十分疼痛，患者經常痛到無法正常走路。</p>

<p class="style1">雞眼的成因中，不合腳的鞋子最為常見，其他還包括穿著高根鞋，怪異的走路姿勢，解剖學上的骨骼排列異常或是先天畸型等。這些因素造成走路時身體重量集中在腳底的少數區域，反覆摩擦因此形成雞眼或是胼胝。</p>


<p class="style1">雞眼治療的目標是減輕疼痛的症狀，找出潛在的原因，避免穿太緊、太高根或是太尖頭的鞋子。臨床上常使用的治療方式為，使用有去角質作用的水楊酸藥膏或是藥水，配合削除一些增厚的角質層，通常都可以達到改善症狀的目標。</p>

<p class="style1">另外還可以採用冷凍、電燒或是雷射治療。如果上述治療方式都失敗，最後才考慮手術切除。但是要注意的是，如果潛在的原因沒有去除，切除後仍然有復發的可能性。</p>

<p class="style1">臨床上，雞眼常常和腳底的病毒疣（足疣）混淆，不僅患者搞不清楚，許多的醫生也分辨不清。足疣通常是由人類乳突狀病毒的第一型、二型、四型以及五十七型所引起。我們將雞眼和足疣的鑑別診斷要點整理如表。</p>


<p class="style1">足疣因為可能會傳染到其他皮膚的部位，治療上可以採取液態氮冷凍治療，通常每隔1~2週做一次，平均經過3~4次的治療可以治癒。對於角質較厚的病灶，冷凍治療之外，可以配合使用去角質的水楊酸來治療。另外，新一代的刺激局部皮膚免疫的藥膏（樂得美），可經由激發皮膚的免疫反應，來達到排除皮膚的病毒感染以及皮膚癌的作用，是治療病毒疣的新選擇。</p>

