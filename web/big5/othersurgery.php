    <td width="61%" valign="top" id="#2"><p><strong class="t">嵌甲</strong><br />

<p class="style1">嵌甲也就是俗稱的「生倒甲」，有這種情況的患者，幾乎都無可避免的會引起甲溝炎。</p>

<p class="style1">造成嵌甲的原因主要有四項，最常見的是職業因素或鞋子過窄或過緊；第二是不當的指甲修剪；第三是先天性甲面過寬；第四是卷甲引起。</p>

<p class="style1">大部分的患者，尤其是第一或第二項原因所造成的，只要適當休息，改穿前端較為寬鬆的鞋子，以及正確修剪指甲，就能夠有效改善。但是如果是反覆發作，或是第三、四項原因所引起的，就可能須要手術治療。</p>

<p class="style1">傳統的嵌甲手術，僅止於對患部指甲的部分或全部拔除，不僅容易復發，而且因為手法粗暴，時常給患者帶來很大的痛苦。</p>

<p class="style1">我們使用改良式嵌甲手術，主要有下列特點：</p>

<p class="style1">第一，這是根除手術，免去再發的困擾。</p>

<p class="style1">第二，可以降低患者在術中，術後的疼痛不適。七成的患者在術後不會或僅有輕微疼痛，其餘患者在術後也僅有三到四天的疼痛期。</p>

<p class="style1">第三，治療仍兼顧美觀。術後一週內即可恢復指甲外觀，將術後恢復期對日常生活的影響降到最低。患者在手術四天後即可穿著皮鞋，在適當保護下可直接淋浴，可以正常走動，甚至小跑步。</p>

<p class="style1">手術的麻醉採用四點式浸潤麻醉，能改善傳統局部麻醉或神經阻斷法麻醉不完全的缺點，患者在術中不會感覺到疼痛。</p>

<p class="style1">手術包括幾個部分。第一，是將嵌甲處的指甲作部分的拔除；第二，針對指甲的生長點作甲床整形手術，使新生的甲面寬度縮小，達到避免再發的目的。術後使用藥膏包紮，有收斂，止痛，促進傷口癒合的功效。</p>

<p class="style1">這半年多來，我們已經使用改良式的嵌甲手術，成功治療了十數位在傳統治療下無效的復發性嵌甲患者，患者術後的舒適度和滿意度都相當高。半年多追蹤下來，僅有一位短暫性復發，經過我們的指甲護理後，也得到不錯的治療結果。	</p>

<p class="style1">預防嵌甲有三個好辦法：</p>
<p class="style1">一、養成良好的剪趾甲習慣，要將趾甲修剪成平直樣，而不是圓弧行。</p>
<p class="style1">二、選擇相對寬鬆的鞋，使腳趾在鞋裏有一定的活動空間，感受不到任何壓力。</p>
<p class="style1">三、養成良好的衛生習慣，及時去除甲溝裏的污垢，保持趾甲的清潔。</p>

